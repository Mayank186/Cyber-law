      <h3>Corporate law (also known as business law or enterprise law or sometimes company law) is
           the body of law governing the rights, relations, and conduct of persons, companies, organizations
            and businesses. It refers to the legal practice relating to, or the theory of corporations. Corporate 
            law often describes the law relating to matters which derive directly from the life-cycle of a corporation.
            It thus encompasses the formation, funding, governance, and death of a corporation.</h3>
      <h3>Corporate law regulates how corporations, investors, shareholders, directors, employees, creditors,
