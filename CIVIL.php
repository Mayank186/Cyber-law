  <h2 style="font-family:arial;"><b>Types of civil law offenses</b></h2>
    
      <h4>Civil law cases are divided into four main categories, each covering a range of issues.  See below for the types 
          of civil cases and corresponding civil law examples.</h4>
        <div class="col-sm-8">
           <h3>Contract Law -</h3>
        <h4>Contract law deals with agreements between two or more parties, each of which is obligated to hold up their portion of the agreement.
             For example, two parties enter into an agreement for the lease of an apartment. The Lessor has the right to use the apartment,
              and the landlord receives rent money as compensation. If one party violates any of the provisions of the contract, they have
               committed a civil wrong known as “breach of contract.” Generally speaking, contracts may be oral or written, however there are
                certain types of contracts that must be put in writing.</h4>
           
            </div>
            <div class="col-sm-4">
                <img style="max-width:300px; margin-top: 25px;"
