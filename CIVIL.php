        <h3>Family Law -</h3>
        <h4>Family law is the branch of civil law that deals with marriage, divorce, annulment, child custody, adoption, birth, 
        child support, and any other issues affecting families. This branch of civil law is unique in that there is not necessarily 
        a person who committed a civil wrong. This is particularly true in states that have no-fault divorces. The family court gets
         involved with dividing up property and finances after a divorce, establishing child custody, child support, and spousal support
          among other things. Some newer areas that fall under the family law umbrella are same-sex marriage, artificial conception, surrogate
           motherhood, in vitro fertilization, and palimony.</h4>
            
