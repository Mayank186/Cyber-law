        <div class="col-sm-8">
            <h3>Property Law -</h3>
        <h4>Property law covers both personal property and real property. Personal property can be tangible, such as jewelry, 
        animals, and merchandise, or intangible such as patents, copyrights, stocks, and bonds. Real property refers to land and 
        anything built on it that cannot be easily removed, as well as anything under the surface of the land, such as oil and minerals. </h4>
        <h4>There are two types of property law torts: trespass and conversion:</h4>
        <ul style="list-style-type: circle;">
              <li>
              Trespass to chattels refers to a defendant intentionally and physically interfering with the plaintiff’s
               right to possession and use of their personal property. 
                </li>
                <li>
                Trespass to land occurs when a defendant enters plaintiff’s private property without consent of the plaintiff
                </li>
                <li>
                Conversion refers to a defendant depriving a plaintiff of their personal property without the plaintiff’s consent,
                 and then using the plaintiff’s property as his own.
                </li>
                </ul>
        </div>
