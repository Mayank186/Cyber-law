        <div class="col-sm-8">
        <h3>Tort Law -</h3>
        <h4>Tort law is a branch of civil law that is concerned with personal injury and civil wrongdoing. 
            A tort is a civil wrong, done by one person or entity to another which results in injury or property damage, and
             frequently involves monetary compensation to the injured party. There are three categories of torts: negligence,
              intentional tort, and strict liability.</h4>
              <ul style="list-style-type: circle;">
              <li>
              Intentional tort -
              An intentional tort is a deliberate wrongdoing in which the defendant acted with intent to cause harm or injury.
               Some examples of intentional torts include: assault and battery, false imprisonment, fraud, invasion of privacy,
                and intentional infliction of emotional distress. 
                </li>
                <li>
                Negligence - It is an unintentional tort, to which there are four elements that must be satisfied.
                </li>
                <li>
                Strict liability - It is a tort that does not require actual negligence or 
                intent to injure. It is based on an absolute or “strict” duty to ensure something is safe 
                </li>
                </ul>
        </div>
        <div class="col-sm-4"><br>
